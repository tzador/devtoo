<h1>Chrome Extension</h1>

<p><a href="/extension.zip">Download</a> and unzip the chrome extension</p>
<p>
  Goto <a href="chrome://extensions">chrome://extensions</a> and load the unpacked extension from the
  folder where you unzipped it.
</p>
<p>
  Whenever you visit a dev.to article, click the extension icon to see related articles. It also
  works for any other webpage, but the results might be less relevant.
</p>
